def seed():
    with app.app_context():
        if Teacher.query.first():
            print('Database already seeded. Skipping.')
            return

        # Create demo teacher
        teacher = Teacher(login='teacher', display_name='Демо Учитель')
        teacher.set_password('teacher123')
        db.session.add(teacher)
        db.session.flush()

        # Create tests for all grades
        for grade, tests_list in TESTS_BY_GRADE.items():
            for title, topic in tests_list:
                test = Test(
                    title=title,
                    grade=grade,
                    topic=topic,
                    created_by=teacher.id,
                    is_published=True,
                    settings={
                        'show_answer': False,
                        'max_attempts': 2,
                        'shuffle_questions': False,
                        'shuffle_answers': False,
                        'show_correct_answers': False,
                        'show_score': True,
                    },
                )
                db.session.add(test)
                db.session.flush()

                # Add sample questions to first test of grade 5
                if grade == 5 and '01' in title:
                    for i, q_data in enumerate(SAMPLE_QUESTIONS_5_TEST_01):
                        question = Question(
                            test_id=test.id,
                            order=i + 1,
                            question_type=q_data['question_type'],
                            content=q_data['content'],
                            correct_answer=q_data['correct_answer'],
                            points=q_data['points'],
                        )
                        db.session.add(question)

        db.session.commit()
        print('Seed complete!')
        print(f'  Teacher: login=teacher, password=teacher123')
        print(f'  Tests created: {sum(len(t) for t in TESTS_BY_GRADE.values())}')


if __name__ == '__main__':
    seed()
